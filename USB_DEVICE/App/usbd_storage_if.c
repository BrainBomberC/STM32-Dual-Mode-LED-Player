/**
  * @brief  Initializes the storage unit (medium) over USB FS IP
  * @param  lun: Logical unit number.
  * @retval USBD_OK if all operations are OK else USBD_FAIL
  */
int8_t STORAGE_Init_FS(uint8_t lun)
{
  /* USER CODE BEGIN 2 */

   // 1. 检查 SD 卡是否已经初始化好了
  // 如果在 main.c 里已经初始化成功了，这里直接返回 OK，秒回！
  // HAL_SD_GetCardState 返回 HAL_SD_CARD_TRANSFER 表示卡准备好读写了
  if (HAL_SD_GetCardState(&hsd) == HAL_SD_CARD_TRANSFER)
  {
      return (USBD_OK);
  }

  // 2. 如果还没初始化（或者之前初始化失败了），再尝试初始化
  // 这里的 hsd.Init.ClockDiv 保持你在 CubeMX 里的配置 (118 或 4)
  if (HAL_SD_Init(&hsd) != HAL_OK)
  {
      return (USBD_FAIL);
  }

  // 3. 提速 (如果在 main 里没提速，这里补救一下)
  // 如果你在 CubeMX 里已经是 118 了，这里必须提速，否则读写太慢
  /*
  __HAL_SD_DISABLE(&hsd);
  hsd.Init.ClockDiv = 4; // 8MHz
  SDIO->CLKCR = (SDIO->CLKCR & 0xFFFFFF00) | 4; 
  __HAL_SD_ENABLE(&hsd);
  */

  return (USBD_OK);
  /* USER CODE END 2 */
}

/**
  * @brief  Returns the medium capacity.
  * @param  lun: Logical unit number.
  * @param  block_num: Number of total block number.
  * @param  block_size: Block size.
  * @retval USBD_OK if all operations are OK else USBD_FAIL
  */
int8_t STORAGE_GetCapacity_FS(uint8_t lun, uint32_t *block_num, uint16_t *block_size)
{
  /* USER CODE BEGIN 3 */
  HAL_SD_CardInfoTypeDef info;
  
  if(HAL_SD_GetCardInfo(&hsd, &info) != HAL_OK)
  {
      return USBD_FAIL;
  }

  *block_num  = info.LogBlockNbr - 1;
  *block_size = info.LogBlockSize;
  return (USBD_OK);
  /* USER CODE END 3 */
}

